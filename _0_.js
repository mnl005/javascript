// 자바스크립트 코드 처리 과정
// 실행 컨텍스트 ????
// 1. 파싱 및 컴파일
// 2. 인터프리터로 바이트코드 바로 실행, JIT컴파일러로 자주 실행되는 코드를 네이티브 코드로 변환하여 성능 최적화
// 3. 변수 처리
// ... var로 선언된 변수는 런타임 이전 undefined로 초기화되며 함수스코프 가짐
// ... let, const로 선언된 변수는 블록스코프 가지며 초기화 전 접근 불가
// ... 원시타입 메모리에 할당시 값이 스택에 저장
// ... 참조타입 메모리에 할당시 객체가 힙에 저장되고 스택에 참조값 저장
// ... 더이상 참조 않는 변수는 가비지 컬렉션에 의해 메모리에서 해제
// 4. 함수 처리
// ... 실행 이전 선언이 호이스팅, 실행시 콜스택에 기록, 실행 이후 스택에서 제거,
// 클로저는 함수가 선언된 스코프의 변수를 참조할 수 있는 상태를 유지
// 5. 비동기 처리
// ... 자바스크립트는 단일 스레드로 동작, 비동기 작업은 이벤트 루프를 통해 처리
// ... 콜스택에 코드 푸시 후 실행 -> 비동기 작업(타이머) 등은 백그라운드에서 처리 -> 완료작업은 태스크 큐에 푸시 -> 이벤트 루프가 호출 스택이 비었는지 확인 후 태스크 큐의 작업 실행
// ... 비동기 함수 - setTimeout, fetch, Promise, async/await
// 6. 이벤트 루프 - 비동기 작업 처리 매커니즘
// ... 콜스택 - 실행중인 함수 저장
// ... 태스크 큐 - 완료된 비동기 작업 저장
// ... 마이크로태스크 큐 - Promise 와 MutationObserver 저장
// ... 이벤트루프 - 호출 스택이 비면 태스크 큐에서 작업을 가져와 실행
// 7. 모듈 로드 - import/export 키워드 사용해 모듈을 비동기적으로 로드
// 8. 메모리 관리
// ... 스택 - 함수 호출, 원시값 저장
// ... 힙 - 객체와 참조값 저장
